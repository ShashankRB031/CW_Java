import java.util.Stack;

public abstract class StackByRecursively {
    public static void main(String[] args) {
         Stack<Integer> st = new Stack<>();  
        st.push(1); // push 1
        st.push(2); // push 2
        st.push(3); // push 3
        st.push(4); // push 4
        st.push(5); // push 5
        System.out.println(st); // print original stack

    }
}
